class Browser < AccessManagementSuper
  #############################################################################
  # 更新項目設定
  #############################################################################
  attr_accessible :browser_name
  #############################################################################
  # リレーション設定
  #############################################################################
  # ブラウザバージョン
  has_many :browser_version
  # リクエスト解析結果
  has_many :request_analysis_result
  
  #############################################################################
  # バリデーション定義
  #############################################################################
  validates :browser_name,
    :presence => true,
    :length => {:maximum => 255}
end
