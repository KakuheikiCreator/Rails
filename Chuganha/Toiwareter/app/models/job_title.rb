class JobTitle < ActiveRecord::Base
  #############################################################################
  # 更新項目設定
  #############################################################################
  attr_accessible :job_title, :lock_version
  
  #############################################################################
  # バリデーション定義
  #############################################################################
  validates :job_title,
    :presence => true,
    :length => {:maximum => 255}
end
