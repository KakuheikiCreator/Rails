class Newspaper < ActiveRecord::Base
  #############################################################################
  # 更新項目設定
  #############################################################################
  attr_accessible :newspaper_name, :lock_version
  
  #############################################################################
  # 定数定義
  #############################################################################
  # ID（その他）
  ID_OTHER = 30
  
  #############################################################################
  # バリデーション定義
  #############################################################################
  validates :newspaper_name,
    :presence => true,
    :length => {:maximum => 255}
end
