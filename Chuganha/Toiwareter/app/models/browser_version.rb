class BrowserVersion < AccessManagementSuper
  #############################################################################
  # 更新項目設定
  #############################################################################
  attr_accessible :browser_id, :browser_version
  
  #############################################################################
  # リレーション設定
  #############################################################################
  # ブラウザ
  belongs_to :browser
  # リクエスト解析結果
  has_many :request_analysis_result
  
  #############################################################################
  # バリデーション定義
  #############################################################################
  validates :browser_id,
    :presence => true
  validates :browser_version,
    :length => {:maximum => 255}
end
