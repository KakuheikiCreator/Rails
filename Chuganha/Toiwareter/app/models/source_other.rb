class SourceOther < ActiveRecord::Base
  #############################################################################
  # 更新項目設定
  #############################################################################
  attr_accessible :source_id, :quote_id, :media_name, :lock_version
  
  #############################################################################
  # リレーション設定
  #############################################################################
  # Quote
  belongs_to :quote
  
  #############################################################################
  # バリデーション定義
  #############################################################################
  validates :source_id,
    :presence => true
  validates :quote_id,
    :presence => true
  validates :media_name,
    :presence => true,
    :length => {:maximum => 60}
  
  #############################################################################
  # Solr設定
  #############################################################################
  searchable do
    text :media_name
  end
  
  #############################################################################
  # public定義
  #############################################################################
  public
  # 履歴生成
  def new_history
    ent = SourceHistoryOther.new
    ent.source_id  = self.source_id
    ent.media_name = self.media_name
    return ent
  end
end
