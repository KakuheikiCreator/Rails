class Source < ActiveRecord::Base
  #############################################################################
  # 更新項目設定
  #############################################################################
  attr_accessible :source, :lock_version
  
  #############################################################################
  # バリデーション定義
  #############################################################################
  validates :source,
    :presence => true,
    :length => {:maximum => 255}
end
