class Country < ActiveRecord::Base
  #############################################################################
  # 更新項目設定
  #############################################################################
  attr_accessible :country_name_cd, :country_name, :lock_version
    
  #############################################################################
  # バリデーション定義
  #############################################################################
  validates :country_name_cd,
    :presence => true,
    :length => {:is => 2}
  validates :country_name,
    :presence => true,
    :length => {:maximum => 255}
end
